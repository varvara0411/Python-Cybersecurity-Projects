import re

# считываем строку лога из стандартного ввода
log_line = input()

# определяем паттерн для поиска:
#   "user=" - литеральная строка
#   (...)   - начало захватывающей группы
#   [a-zA-Z0-9]+ - один или более символов (буква или цифра) - это имя пользователя
#   (...)   - конец захватывающей группы
pattern = r"user=([a-zA-Z0-9_-]+)"

# ищем ПЕРВОЕ вхождение этого паттерна в строке лога
match = re.search(pattern, log_line)

# проверяем, было ли найдено совпадение
if match is not None:
    # если совпадение найдено (match не None),
    # извлекаем текст, совпавший с ПЕРВОЙ (и единственной)
    # захватывающей группой (индекс 1)
    username = match.group(1)
    # выводим извлеченное имя пользователя
    print(username)

# если совпадение не найдено (match равен None),
# блок if не выполнится, и программа ничего не выведет.

import re
import sys

# определяем паттерн для формата даты ГГГГ-ММ-ДД
#   \d{4} - ровно 4 цифры
#   -     - дефис
#   \d{2} - ровно 2 цифры
date_pattern = r"\d{4}-\d{2}-\d{2}"

# сначала читаем все строки из ввода
lines = sys.stdin.readlines()

# обрабатываем текст построчно
for line in lines:
    # используем re.match() чтобы проверить, начинается ли
    # текущая строка 'line' с нашего паттерна даты.
    match = re.match(date_pattern, line)

    # если совпадение найдено в начале строки (match не None)
    if match:
        # выводим всю исходную строку 'line'.
        # используем end='', так как 'line' уже содержит
        # символ перевода строки \n в конце.
        print(line, end='')

# Если строка не начинается с паттерна, она просто пропускается.


import re

# считываем строку лога из стандартного ввода
log_line = input()

# определяем паттерн для поиска:
#   "pid=" - литеральная строка
#   (     - начало захватывающей группы
#   \d+   - одна или более цифр (0-9)
#   )     - конец захватывающей группы
pattern = r"pid=(\d+)"

# ищем ПЕРВОЕ вхождение этого паттерна в строке лога
match = re.search(pattern, log_line)

# проверяем, было ли найдено совпадение
if match: 
    # если совпадение найдено, извлекаем текст,
    # совпавший с ПЕРВОЙ (и единственной) захватывающей группой (индекс 1).
    # это будет строка, содержащая число PID.
    pid_value = match.group(1)
    # выводим извлеченное значение PID
    print(pid_value)

# если совпадение не найдено (match равен None),
# блок if не выполнится, и программа ничего не выведет.
